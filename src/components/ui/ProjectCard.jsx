import React, { useState, useRef } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { Github, Sparkles } from 'lucide-react';

// Spark Component for button click effect
const Spark = ({ x, y, _id, color }) => {
  return (
    <motion.div
      className="absolute rounded-full pointer-events-none"
      initial={{ 
        x: x, 
        y: y, 
        scale: 0.1, 
        opacity: 1, 
        backgroundColor: color,
        boxShadow: `0 0 5px ${color}, 0 0 10px ${color}`
      }}
      animate={{ 
        scale: [0.1, 1, 0], 
        opacity: [1, 0.5, 0], 
        x: [x, x + (Math.random() - 0.5) * 50],
        y: [y, y + (Math.random() - 0.5) * 50]
      }}
      exit={{ opacity: 0 }}
      transition={{ duration: 0.6, ease: "easeOut" }}
      style={{
        width: '8px',
        height: '8px',
      }}
    />
  );
};

// Data Flow Animation Component
const DataFlowAnimation = ({ isHovered }) => {
  return (
    <div className="absolute inset-0 overflow-hidden pointer-events-none">
      {[...Array(6)].map((_, i) => (
        <motion.div
          key={i}
          className="absolute w-1 bg-purple-400"
          style={{
            left: `${15 * i + 10}%`,
            top: '-5px',
          }}
          animate={isHovered ? {
            y: [0, 400],
            opacity: [0, 1, 1, 0],
            scale: [1, 1.5, 1, 0.5]
          } : {}}
          transition={{
            duration: 2,
            repeat: isHovered ? Infinity : 0,
            delay: i * 0.2,
            ease: "easeInOut"
          }}
        />
      ))}
    </div>
  );
};

// Circuit Pattern Component
const CircuitPattern = ({ _isHovered }) => {
  return (
    <svg className="absolute inset-0 w-full h-full opacity-0 group-hover:opacity-20 transition-opacity duration-500 pointer-events-none">
      <defs>
        <pattern id="circuit" x="0" y="0" width="40" height="40" patternUnits="userSpaceOnUse">
          <circle cx="5" cy="5" r="1" fill="currentColor" className="text-purple-400" />
          <circle cx="35" cy="5" r="1" fill="currentColor" className="text-purple-400" />
          <circle cx="5" cy="35" r="1" fill="currentColor" className="text-purple-400" />
          <circle cx="35" cy="35" r="1" fill="currentColor" className="text-purple-400" />
          <line x1="5" y1="5" x2="35" y2="5" stroke="currentColor" strokeWidth="0.5" className="text-purple-400" />
          <line x1="5" y1="5" x2="5" y2="35" stroke="currentColor" strokeWidth="0.5" className="text-purple-400" />
        </pattern>
      </defs>
      <rect width="100%" height="100%" fill="url(#circuit)" />
    </svg>
  );
};

const ProjectCard = ({ project, index, onViewDetails }) => {
  const [isHovered, setIsHovered] = useState(false);
  const [sparks, setSparks] = useState([]);
  const sparkIdCounter = useRef(0); // Add a ref for a unique ID counter

  const handleSpark = (e) => {
    const rect = e.currentTarget.getBoundingClientRect();
    const x = e.clientX - rect.left;
    const y = e.clientY - rect.top;
    const newSpark = {
      id: sparkIdCounter.current++, // Use the incrementing counter
      x: x,
      y: y,
      color: Math.random() > 0.5 ? '#a855f7' : '#ec4899',
    };
    setSparks((prevSparks) => [...prevSparks, newSpark]);
    setTimeout(() => {
      setSparks((prevSparks) => prevSparks.filter((spark) => spark.id !== newSpark.id));
    }, 600); // Remove spark after animation
  };

  return (
    <motion.div
      className="group relative rounded-xl overflow-hidden border-2 border-purple-500/40 transition-all duration-300 backdrop-blur-sm min-h-[25rem]"
      initial={{ opacity: 0, y: 50, rotateX: 0, rotateY: 0, boxShadow: "0px 0px 0px rgba(0,0,0,0)" }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true, margin: "-100px" }}
      transition={{ 
        duration: 0.5, 
        delay: index * 0.1,
        type: "spring",
        stiffness: 100
      }}
      onHoverStart={() => setIsHovered(true)}
      onHoverEnd={() => setIsHovered(false)}
      whileHover={{
        scale: 1.03,
        rotateX: 2,
        rotateY: 2,
        boxShadow: "0 10px 30px rgba(168, 85, 247, 0.6)",
        transition: {
          type: "spring",
          stiffness: 200,
          damping: 10
        }
      }}
      style={{
        transformPerspective: '1000px', // Establishes the 3D perspective
      }}
    >

      
      {/* Background and Inner Content */}
      <div className="relative bg-slate-100 dark:bg-slate-800/50 rounded-xl h-full w-full flex flex-col">
        {/* Background Effects */}
        <DataFlowAnimation isHovered={isHovered} />
        <CircuitPattern isHovered={isHovered} />
        


        {/* Project Image/Icon */}
        <motion.div
          className="relative text-6xl p-8 bg-gradient-to-br from-purple-500/20 to-pink-500/20 text-center overflow-hidden h-48 flex items-center justify-center"
          whileHover={{ scale: 1.05 }}
          transition={{ duration: 0.3 }}
        >
            <motion.div
              animate={isHovered ? { rotate: 360 } : { rotate: 0 }}
              transition={{ duration: 20, repeat: Infinity, ease: "linear" }}
            >
              {project.image}
            </motion.div>
          
          {/* Corner Accent */}
          <motion.div 
            className="absolute top-0 right-0 w-20 h-20 bg-gradient-to-br from-purple-500 to-transparent opacity-0 group-hover:opacity-30"
            initial={{ scale: 0, rotate: 0 }}
            whileHover={{ scale: 1, rotate: 45 }}
            transition={{ duration: 0.3 }}
          />
        </motion.div>

        <div className="relative p-6 flex flex-col flex-grow">
          {/* Title and Badge */}
          <div className="flex items-center justify-between mb-3">
            <motion.h3 
              className="text-xl font-bold"
              initial={{ x: -20, opacity: 0 }}
              whileInView={{ x: 0, opacity: 1 }}
              transition={{ delay: index * 0.1 + 0.2 }}
            >
              {project.title}
            </motion.h3>
            {project.featured && (
              <motion.span 
                className="px-2 py-1 text-xs bg-purple-500 text-white rounded-full flex items-center gap-1"
                initial={{ scale: 0 }}
                whileInView={{ scale: 1 }}
                transition={{ delay: index * 0.1 + 0.3, type: "spring" }}
                whileHover={{ scale: 1.1 }}
              >
                <Sparkles size={12} />
                Featured
              </motion.span>
            )}
          </div>

          {/* Description */}
          <motion.p 
            className="text-gray-700 dark:text-gray-300 mb-4 text-sm leading-relaxed flex-grow"
            initial={{ opacity: 0 }}
            whileInView={{ opacity: 1 }}
            transition={{ delay: index * 0.1 + 0.3 }}
          >
            {project.description}
          </motion.p>
          
          {/* Technologies */}
          <div className="flex flex-wrap gap-2 mb-4 flex-grow-0">
            {project.technologies.map((tech, i) => (
              <motion.span 
                key={`${project.id}-${tech}`}
                className="px-2 py-1 text-xs bg-slate-400/20 dark:bg-slate-700/50 rounded-md text-gray-600 dark:text-gray-400 border border-purple-500/0 hover:border-purple-500/30 transition-colors cursor-default"
                initial={{ opacity: 0, scale: 0 }}
                whileInView={{ opacity: 1, scale: 1 }}
                viewport={{ once: true }}
                transition={{ delay: index * 0.1 + 0.4 + i * 0.05 }}
                whileHover={{ 
                  scale: 1.1, 
                  y: -2,
                  backgroundColor: 'rgba(168, 85, 247, 0.1)'
                }}
              >
                {tech}
              </motion.span>
            ))}
          </div>
          
          {/* Action Buttons */}
          <motion.div 
            className="flex gap-3 mt-auto"
            initial={{ y: 20, opacity: 0 }}
            whileInView={{ y: 0, opacity: 1 }}
            transition={{ delay: index * 0.1 + 0.5 }}
          >
            <motion.button
              onClick={(e) => {
                onViewDetails();
                handleSpark(e);
              }}
              className="group/btn flex items-center gap-2 px-4 py-2 bg-purple-600 hover:bg-purple-700 rounded-lg text-sm transition-all flex-1 justify-center text-white relative overflow-hidden"
              whileHover={{ scale: 1.05 }}
              whileTap={{ scale: 0.95 }}
            >
              <Sparkles size={16} className="relative z-10 group-hover/btn:animate-pulse" />
              <span className="relative z-10">Details</span>
              <AnimatePresence>
                {sparks.map((spark) => (
                  <Spark key={spark.id} {...spark} />
                ))}
              </AnimatePresence>
            </motion.button>
            <motion.a 
              href={project.github} 
              target="_blank" 
              rel="noopener noreferrer"
              className="group/btn flex items-center gap-2 px-4 py-2 bg-slate-700 hover:bg-slate-600 rounded-lg text-sm transition-all flex-1 justify-center relative overflow-hidden"
              whileHover={{ scale: 1.05 }}
              whileTap={{ scale: 0.95 }}
              onClick={handleSpark}
            >
              <Github size={16} className="relative z-10 group-hover/btn:rotate-12 transition-transform" />
              <span className="relative z-10">Code</span>
              <motion.div 
                className="absolute inset-0 bg-gradient-to-r from-slate-600 to-slate-700"
                initial={{ x: "-100%" }}
                whileHover={{ x: 0 }}
                transition={{ duration: 0.3 }}
              />
              <AnimatePresence>
                {sparks.map((spark) => (
                  <Spark key={spark.id} {...spark} />
                ))}
              </AnimatePresence>
            </motion.a>
            
          </motion.div>
        </div>
      </div>
    </motion.div>
  );
};

export default ProjectCard;