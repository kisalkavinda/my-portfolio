import React, { useState } from 'react'
import { motion } from 'framer-motion'
import { Award, Calendar, ZoomIn } from 'lucide-react'
import CertificateModal from '../ui/CertificateModal'
import { certificates } from '../../data/certificates'

const CertificateCard = ({ certificate, index, onViewClick }) => {
  

  return (
    <motion.div
      className="group relative bg-slate-100 dark:bg-slate-800/50 backdrop-blur-sm rounded-xl p-6 border-2 border-purple-500/40 hover:border-purple-500/50 transition-all overflow-hidden flex flex-col"
      initial={{ opacity: 0, y: 50 }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true, margin: "-50px" }}
      transition={{ 
        duration: 0.5, 
        delay: index * 0.1,
        type: "spring",
        stiffness: 100
      }}
      whileHover={{ y: -8 }}
      
    >
      <div className="absolute inset-0 bg-gradient-to-br from-purple-500/0 to-pink-500/0 group-hover:from-purple-500/5 group-hover:to-pink-500/5 transition-all duration-500" />



      <div className="relative z-10 flex-grow">
        <div className="flex items-start justify-between mb-4">
          <motion.div 
            className="text-5xl"
            whileHover={{ rotate: [0, -10, 10, 0], scale: 1.1 }}
            transition={{ duration: 0.5 }}
          >
            {certificate.icon}
          </motion.div>
          <div className="flex items-center gap-2 text-sm text-gray-600 dark:text-gray-400">
            <Calendar size={16} />
            {certificate.date}
          </div>
        </div>

        <h3 className="text-xl font-bold mb-2 group-hover:text-purple-400 transition-colors">
          {certificate.title}
        </h3>

        <p className="text-sm text-purple-400 font-semibold mb-3 flex items-center gap-1">
          <Award size={16} />
          {certificate.issuingOrg}
        </p>

        <p className="text-sm text-gray-700 dark:text-gray-300 mb-4 leading-relaxed">
          {certificate.description}
        </p>

        <div className="flex flex-wrap gap-2 mb-4">
          {certificate.skills.map((skill) => (
            <span
              key={skill}
              className="px-2 py-1 text-xs bg-purple-500/10 border-2 border-purple-500/20 rounded-full text-gray-600 dark:text-gray-400"
            >
              {skill}
            </span>
          ))}
        </div>
      </div>

      <div className="flex gap-2 mt-auto z-20">
        <motion.button
          onClick={() => onViewClick(certificate)}
          className="flex-1 inline-flex items-center justify-center gap-2 text-purple-400 hover:text-purple-300 font-semibold py-2 px-4 bg-purple-500/10 hover:bg-purple-500/20 rounded-lg transition-all"
          whileHover={{ scale: 1.05 }}
          whileTap={{ scale: 0.95 }}
        >
          <ZoomIn size={16} />
          View Certificate
        </motion.button>
      </div>
    </motion.div>
  )
}

const Certificates = () => {
  const [selectedCertificate, setSelectedCertificate] = useState(null)

  const handleViewClick = (certificate) => {
    setSelectedCertificate(certificate);
  };

  const handleCloseModal = () => {
    setSelectedCertificate(null);
  };

  return (
    <>
      <section id="certificates" className="py-20 px-4 relative overflow-hidden">
        <div className="absolute inset-0 opacity-5">
          <div className="absolute top-20 right-10 w-72 h-72 bg-purple-500 rounded-full blur-3xl" />
          <div className="absolute bottom-20 left-10 w-96 h-96 bg-pink-500 rounded-full blur-3xl" />
        </div>

        <div className="max-w-6xl mx-auto w-full relative z-10">
          <motion.div
            className="text-center mb-16"
            initial={{ opacity: 0, y: 50 }}
            whileInView={{ opacity: 1, y: 0 }}
            viewport={{ once: true }}
            transition={{ duration: 0.6 }}
          >
            <h2 className="text-4xl md:text-5xl font-bold mb-4">
              My <span className="bg-gradient-to-r from-purple-400 to-pink-400 bg-clip-text text-transparent">Certificates</span>
            </h2>
            <p className="text-gray-600 dark:text-gray-400 max-w-2xl mx-auto">
              A collection of my certifications and achievements.
            </p>
          </motion.div>

          <div className="grid md:grid-cols-2 gap-6">
            {certificates.map((cert, index) => (
              <CertificateCard 
                key={index} 
                certificate={cert} 
                index={index}
                onViewClick={handleViewClick}
              />
            ))}
          </div>
        </div>
      </section>

      <CertificateModal
        certificate={selectedCertificate}
        onClose={handleCloseModal}
      />
    </>
  )
}

export default Certificates;
